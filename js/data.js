var convertINDC = function(data) {
    var res = [];
    for (var i = 0; i < data.length; i++) {
        if (data[i][1]) {
            res.push({
                name: data[i][0],
                value: data[i][1],
                base: data[i][2]
            });
        }
    }
    return res;
};